void fb_update_cursor(unsigned new_pos)
{
	unsigned col = _fb_cursor % _window_char_width;
	unsigned row = _fb_cursor / _window_char_width;
	char val = _fb_text[_fb_cursor];

	if (val == ' ' || val == '\0' || val == '\n' || val == '\r' ||
	    val == '\t')
		fb_write_char(col, row, 130, VGA_COLOR_BLACK,
			      _fb_bg_color[_fb_cursor]);
	else
		fb_write_char(col, row, val, _fb_color[_fb_cursor],
			      _fb_bg_color[_fb_cursor]);

	_fb_cursor = new_pos;
	val = _fb_text[_fb_cursor];
	col = _fb_cursor % _window_char_width;
	row = _fb_cursor / _window_char_width;

	if (val == ' ' || val == '\0' || val == '\n' || val == '\r' ||
	    val == '\t')
		fb_write_char(col, row, 129, VGA_COLOR_WHITE,
			      _fb_bg_color[_fb_cursor]);
	else
		fb_write_char_mix_cursor(col, row, val, _fb_color[_fb_cursor],
					 VGA_COLOR_WHITE);
}

void fb_scroll_line(void)
{
	unsigned copy_size = VGA_RESOLUTION_X *
			     (VGA_RESOLUTION_Y - _fb_font_height) *
			     (VGA_COLOR_DEPTH / 8);
	unsigned row_pixel_size =
		VGA_RESOLUTION_X * _fb_font_height * (VGA_COLOR_DEPTH / 8);
	char *old_row_ptr = (char *)_fb_buffer;
	char *new_row_ptr = old_row_ptr + VGA_RESOLUTION_X *
						  (VGA_COLOR_DEPTH / 8) *
						  _fb_font_height;
	char *last_row = old_row_ptr + copy_size;
	char *next_char_row = _fb_text + _window_char_width;
	unsigned txt_copy_size = _window_char_width * (_window_char_height - 1);
	char *last_char_row =
		_fb_text + _window_char_width * (_window_char_height - 1);
	char val;

	/* clear cursor display before pixel blit */
	val = _fb_text[_fb_cursor];
	if (val == ' ' || val == '\0' || val == '\n' || val == '\r' ||
	    val == '\t' || val == '\b')
		fb_write_char(_fb_cursor % _window_char_width,
			      _fb_cursor / _window_char_width, 130,
			      VGA_COLOR_BLACK, _fb_bg_color[_fb_cursor]);

	memcpy(old_row_ptr, new_row_ptr, copy_size);

	/* restore cursor at new position */
	fb_write_char(_fb_cursor % _window_char_width,
		      _fb_cursor / _window_char_width, 129, VGA_COLOR_WHITE,
		      _fb_bg_color[_fb_cursor]);

	memcpy(_fb_text, next_char_row, txt_copy_size);
	memset(last_char_row, ' ', _window_char_width);
	memset(last_row, 0, row_pixel_size);
	memcpy(_fb_color, _fb_color + _window_char_width,
	       txt_copy_size * sizeof(unsigned));
	memset(_fb_color + _window_char_width * (_window_char_height - 1), 0xFF,
	       _window_char_width * sizeof(unsigned));
	memcpy(_fb_bg_color, _fb_bg_color + _window_char_width,
	       txt_copy_size * sizeof(unsigned));
	memset(_fb_bg_color + _window_char_width * (_window_char_height - 1), 0,
	       _window_char_width * sizeof(unsigned));
}

void fb_scroll_region(unsigned top_row, unsigned bot_row)
{
	unsigned region_rows = bot_row - top_row;
	unsigned bytes_per_row =
		VGA_RESOLUTION_X * _fb_font_height * (VGA_COLOR_DEPTH / 8);
	unsigned copy_px_bytes = region_rows * bytes_per_row;
	char *fb = (char *)_fb_buffer;
	char val;

	/* clear cursor display before pixel blit */
	val = _fb_text[_fb_cursor];
	if (val == ' ' || val == '\0' || val == '\n' || val == '\r' ||
	    val == '\t' || val == '\b')
		fb_write_char(_fb_cursor % _window_char_width,
			      _fb_cursor / _window_char_width, 130,
			      VGA_COLOR_BLACK, _fb_bg_color[_fb_cursor]);

	/* scroll pixel rows up by one within [top_row, bot_row] */
	memmove(fb + top_row * bytes_per_row,
		fb + (top_row + 1) * bytes_per_row, copy_px_bytes);
	memset(fb + bot_row * bytes_per_row, 0, bytes_per_row);

	/* restore cursor display */
	fb_write_char(_fb_cursor % _window_char_width,
		      _fb_cursor / _window_char_width, 129, VGA_COLOR_WHITE,
		      _fb_bg_color[_fb_cursor]);

	/* scroll text shadow */
	memmove(_fb_text + top_row * _window_char_width,
		_fb_text + (top_row + 1) * _window_char_width,
		region_rows * _window_char_width);
	memset(_fb_text + bot_row * _window_char_width, ' ',
	       _window_char_width);
	memmove(_fb_color + top_row * _window_char_width,
		_fb_color + (top_row + 1) * _window_char_width,
		region_rows * _window_char_width * sizeof(unsigned));
	memset(_fb_color + bot_row * _window_char_width, 0xFF,
	       _window_char_width * sizeof(unsigned));
	memmove(_fb_bg_color + top_row * _window_char_width,
		_fb_bg_color + (top_row + 1) * _window_char_width,
		region_rows * _window_char_width * sizeof(unsigned));
	memset(_fb_bg_color + bot_row * _window_char_width, 0,
	       _window_char_width * sizeof(unsigned));
}

