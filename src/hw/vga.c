void fb_write_color(int x, int y, unsigned color)
{
	unsigned char i, j;
	unsigned *disp = (unsigned *)_fb_buffer;
	unsigned base_x, base_y;
	x = x * char_width;
	y = y * char_height;

	base_x = _fb_x_off + x;
	base_y = _fb_y_off + y;
	for (i = 0; i < char_height; ++i) {
		unsigned *rowp =
			disp + (base_y + i) * _hw_resolution_x + base_x;
		for (j = 0; j < char_width; ++j) {
			rowp[j] = color;
		}
	}
}

